
public abstract class Calculadora {
	
	protected double soma(double x, double y) {
		return x+y;
	}
	
	protected double subtração(double x, double y) {
		return x-y;
	}
	
	protected double multiplicação(double x, double y) {
		return x*y;
	}
	
	protected double divisão(double x, double y) {
		return x/y;
	}
}
